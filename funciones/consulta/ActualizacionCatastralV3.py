    def obtenerVertices(self, geom):
        n  = 0
        ver = geom.vertexAt(0)
        vertices=[]

        while(ver != QgsPoint(0,0)):
            n +=1
            vertices.append(ver)
            ver=geom.vertexAt(n)

        return vertices

    def obtenerVertices2(self, geom):
        polygon = geom.asPolygon()
        vertices = []

        n = len(polygon[0])

        for i in range(n):
            vertices.append(polygon[0][i])

        return vertices


